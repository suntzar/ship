function love.conf(t)
    t.identity = nil                    -- O nome do diretório de salvamento (string)
    t.version = "11.3"                  -- A versão do LÖVE para a qual este jogo foi feito (string)
    t.console = false                   -- Anexar um console (boolean, apenas Windows)
    t.window.title = "oh my giraffe"    -- O título da janela (string)
    t.window.icon = nil                 -- Caminho para uma imagem para usar como ícone da janela (string)
    t.window.width = 561                -- A largura da janela (número)
    t.window.height = 1246              -- A altura da janela (número)
    t.window.borderless = true          -- Remove todos os visuais de borda da janela (boolean)
    t.window.resizable = false          -- Permite que a janela seja redimensionável pelo usuário (boolean)
    t.window.minwidth = 1               -- Largura mínima da janela se ela for redimensionável (número)
    t.window.minheight = 1              -- Altura mínima da janela se ela for redimensionável (número)
    t.window.fullscreen = true          -- Ativa o modo tela cheia (boolean)
    t.window.fullscreentype = 'desktop' -- Modo de tela cheia padrão ou tela cheia de desktop (string)
    t.window.vsync = true               -- Ativa a sincronização vertical (boolean)
    t.window.msaa = 0                   -- O número de amostras para usar com antialiasing multi-amostrado (número)
    t.window.display = 1                -- Índice do monitor para mostrar a janela (número)
    t.window.highdpi = true             -- Ativa o suporte a alta DPI (boolean)
    t.modules.audio = true              -- Ativa o módulo de áudio (boolean)
    t.modules.event = true              -- Ativa o módulo de eventos (boolean)
    t.modules.graphics = true           -- Ativa o módulo gráfico (boolean)
    t.modules.image = true              -- Ativa o módulo de imagem (boolean)
    t.modules.joystick = true           -- Ativa o módulo de joystick (boolean)
    t.modules.keyboard = true           -- Ativa o módulo de teclado (boolean)
    t.modules.math = true               -- Ativa o módulo de matemática (boolean)
    t.modules.mouse = true              -- Ativa o módulo de mouse (boolean)
    t.modules.physics = false           -- Ativa o módulo de física (boolean)
    t.modules.sound = true              -- Ativa o módulo de som (boolean)
    t.modules.system = true             -- Ativa o módulo de sistema (boolean)
    t.modules.timer = true              -- Ativa o módulo de temporizador (boolean)
    t.modules.window = true             -- Ativa o módulo de janela (boolean)
    t.modules.thread = true             -- Ativa o módulo de thread (boolean)
end
